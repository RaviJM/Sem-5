// Console log
console.log("Hello World!");

// Arithmetic operators
let a = 2;
let b = 3;
console.log("a + b = ", a + b);
console.log("a - b = ", a - b);
console.log("a * b = ", a * b);
console.log("a / b = ", a / b);
console.log("a % b = ", a % b);

// Logical operators
console.log("a > b is ", a > b);
console.log("a < b is ", a < b);
console.log("a >= b is ", a >= b);
console.log("a <= b is ", a <= b);
console.log("a == b is ", a == b);
console.log("a != b is ", a != b);

// Assignment operator
let c = a + b;
console.log("c = ", c);

// String operations
let str1 = "Hello";
let str2 = "World";
console.log(str1 + " " + str2);

// Functions
function add(x, y) {
    return x + y;
}
console.log("add(2, 3) = ", add(2, 3));

// If-else
if (a > b) {
    console.log("a is greater than b");
} else {
    console.log("b is greater than a");
}

// While loop
let i = 0;
while (i < 5) {
    console.log("i = ", i);
    i++;
}

// Do-while loop
let j = 0;
do {
    console.log("j = ", j);
    j++;
} while (j < 5);